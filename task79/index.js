function position(letter){
    let arr_en = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'];
    for (let i = 0; i < arr_en.length; i++) {
   if( arr_en[i]===letter){
       return "Position of alphabet: "+(i+1)
   }
    }
}